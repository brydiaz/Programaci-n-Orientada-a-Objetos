    public void generarFactura(ArrayList<Platillo> compra){
        int valorFinal=0;
       for(int i=0; i<compra.size();i++){
           valorFinal= valorFinal+compra.get(i).getCosto()*compra.get(i).getCantidad();
       }
       double valorConIva= valorFinal+valorFinal*0.13;
       double valorConGanancia= valorConIva+valorConIva*0.35;
       this.jTextArea1.setText("Tu carrito lo conforma: "+"\n"+compra.toString()+"\n"+"Con un valor de: "
               + valorFinal+"\n"+"Pero debes pagar: "+ valorConIva+"\n"+"Debido al IVA\n"+"Y ademÃ¡s 35% debido a nuestra atencion\n"
       +"Para un total de:\n"+valorConGanancia);
    }

